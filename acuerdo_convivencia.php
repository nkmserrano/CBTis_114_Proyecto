<p class="fst-normal"><b>Para que nos sirve</b></p>
<p class="fst-normal">En el entorno escolar, es fundamental 
fomentar un ambiente seguro, respetuoso y propicio para el aprendizaje. Para lograr esto, es necesario establecer acuerdos 
de convivencia que promuevan la buena conducta, el respeto mutuo y la responsabilidad de todos los miembros de la comunidad 
educativa.Los acuerdos de convivencia implican que cada individuo sea responsable de sus acciones y se comprometa a cumplir 
con las normas establecidas.</p>
<center><img src="img/convivencia.jpg" width="450" height="200"></center><br>
<p class="fst-normal"><b>¿Por qué son importantes los acuerdos de convivencia en la escuela?</b></p>
<p>Los acuerdos de convivencia son fundamentales en la escuela, ya que ayudan a establecer normas y expectativas claras para 
todos los estudiantes, docentes y personal administrativo. Estos acuerdos tienen varios beneficios:</p>
<ul>
  <li><b>Promueven un ambiente seguro y respetuoso: </b>Los acuerdos de convivencia establecen límites y normas de comportamiento 
    que permiten a todos los miembros de la comunidad educativa sentirse seguros y respetados.</li>
    <li><b>Fomentan la responsabilidad y el compromiso:</b>Los acuerdos de convivencia implican que cada individuo sea responsable de 
      sus acciones y se comprometa a cumplir con las normas establecidas.</li>
</ul><br>
