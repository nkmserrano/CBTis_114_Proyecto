<p class="fst-normal"><b>¿Que es un plan de estudios?</b></p>
<p class="fst-normal">Un plan de estudio es una forma de planificación que diseña el currículo de contenidos y materias de una carrera, 
    generalmente universitaria, aunque aplica a las enseñanzas que imparten en general los centros de estudios.

</p>
<p class="fst-normal"><b>¿Sobre que trata el plan de estudio del CBTis 114?</b></p>
<p>El plan de estudios nos habla sobre las clases que debera cursar el alumno durante tres años, dependiendo de la especialidad elegidad, asi como tambien
    las competencias profesionales de la carrera y los modulos y submodulos que estara llevando durante su tiempo academico en el plantel del CBTis 114.
</p>

